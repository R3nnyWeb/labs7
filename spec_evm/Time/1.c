void changeLedStatus(uint8_t led){
	if(PORT_ReadInputDataBit(MDR_PORTC, led)){
		PORT_ResetBits(MDR_PORTC, led);
	} else {
		PORT_SetBits(MDR_PORTC, led);
	}
}

